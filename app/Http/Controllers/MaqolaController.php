        if ($id == 1) {
            $maqola = Maqola::all();
            return view('maqolalar.index', compact('maqola'));
        }

        $email = Auth::user()->email;
        $hodim = Hodimlar::where('email', $email)->first();  
        $maqola = Maqola::where('hodim_id', $hodim->id)->get();
        return view('maqolalar.index', compact('maqola'));
    }

